package TheNewWorld.model;

public class Role {

	private String id ;
	//姓名
	private String name ;
	//性别
	private String gender ;
	//职业
	private String vocation ;
	//等级
	private Integer level ;
	//生命值
	private Integer life ;
	//魔力值
	private Integer mana ; 
	//攻击力
	private Integer attack ; 
	//防御力
	private Integer defense ; 
	//体质
	private Integer physique ; 
	//力量
	private Integer power ; 
	//敏捷
	private Integer nimble ; 
	//智力
	private Integer intelligence ; 
	//攻击距离
	private Double attackDistance ; 
	//攻击频率
	private Double attackRate  ; 
	//攻击速度
	private Double attackSpeed  ; 
	//移动速度
	private Double pace ;
	
	//getter and setter
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getVocation() {
		return vocation;
	}
	public void setVocation(String vocation) {
		this.vocation = vocation;
	}
	public Integer getLevel() {
		return level;
	}
	public void setLevel(Integer level) {
		this.level = level;
	}
	public Integer getLife() {
		return life;
	}
	public void setLife(Integer life) {
		this.life = life;
	}
	public Integer getMana() {
		return mana;
	}
	public void setMana(Integer mana) {
		this.mana = mana;
	}
	public Integer getAttack() {
		return attack;
	}
	public void setAttack(Integer attack) {
		this.attack = attack;
	}
	public Integer getDefense() {
		return defense;
	}
	public void setDefense(Integer defense) {
		this.defense = defense;
	}
	public Integer getPhysique() {
		return physique;
	}
	public void setPhysique(Integer physique) {
		this.physique = physique;
	}
	public Integer getPower() {
		return power;
	}
	public void setPower(Integer power) {
		this.power = power;
	}
	public Integer getNimble() {
		return nimble;
	}
	public void setNimble(Integer nimble) {
		this.nimble = nimble;
	}
	public Integer getIntelligence() {
		return intelligence;
	}
	public void setIntelligence(Integer intelligence) {
		this.intelligence = intelligence;
	}
	public Double getAttackDistance() {
		return attackDistance;
	}
	public void setAttackDistance(Double attackDistance) {
		this.attackDistance = attackDistance;
	}
	public Double getAttackRate() {
		return attackRate;
	}
	public void setAttackRate(Double attackRate) {
		this.attackRate = attackRate;
	}
	public Double getAttackSpeed() {
		return attackSpeed;
	}
	public void setAttackSpeed(Double attackSpeed) {
		this.attackSpeed = attackSpeed;
	}
	public Double getPace() {
		return pace;
	}
	public void setPace(Double pace) {
		this.pace = pace;
	} 
	
	
	
}
